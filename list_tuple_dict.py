# collection data type
'''
list is collection data type and is written in square brackets.
'''
colors = ["red" , "green" , "blue" , 3 , 23 , 2.0]
print(colors) # prints list
print(len(colors)) # returns total elements
print(type(colors)) # returns the type

'''
tuple is collection data type and is written in small brackets.
'''
colors = ["red" , "green" , "blue" , "white"]
print(colors)

'''
dictionary is collection data type and is written in curly braces.
'''
dict = {
"number": 1,
"name": "hamza",
"age": 23

}
print(dict)
