# indexing is getting the position of a particular character in string.
sentence = "Anything in between double/single quotes is known as string in Python"
print(sentence[5])
print(sentence.replace("e","a"))

# slicing is cutting any part of string which is required. in general getting
# required information from a paragraph.

sentence = "Anything in between double/single quotes is known as string in Python"
print(sentence[0:19])
print(sentence[-10:-1])
